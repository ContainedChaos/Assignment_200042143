package Assignment2.Independence;

public class Student
{
    public String name;
    public String id;
    public String dept;
    public int semester;
    public float cgpa;

    public Student (String name, String id, String dept, int semester, float cgpa)
    {
        this.name = name;
        this.id = id;
        this.dept = dept;
        this.semester = semester;
        this.cgpa = cgpa;
    }
}
